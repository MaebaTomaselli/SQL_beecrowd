2991

Divisões Com Maiores Médias Salariais

Listar as divisões com maiores médias salariais dentro de seus departamentos. A saída 
deverá apresentar o nome do departamento, o nome da divisão com maior média salarial do 
departamento e a média salarial da divisão. O resultado deve estar em ordem decrescente 
usando a média salarial.

Dica: Você pode utilizar a função COALESCE(check_expression , 0) para substituir algum 
valor null por zero; Além disso, você também pode utilizar a função ROUND(value, 2) 
para exibir os valores com 2 casas decimais.

